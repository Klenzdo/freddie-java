package com.freeman;

public class elseIfStatement {
    public static void main(String[] args) {
        String town = "Port Harcourt";
        if (town == "Abakiliki"){
            System.out.println("Abakiliki");
        }else if (town == "Umuahia"){
            System.out.println("Umuahia");
        }else{
            System.out.println(town);
        }
    }
}
